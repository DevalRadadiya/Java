public class Operators {
    public static void main(String[] args){
        int x=20,y=5,z;

        z=x+y;
        System.out.println(z);

        System.out.println(x=x+3);
        System.out.println(x+=3);
        System.out.println(x-=3);
        System.out.println(x*=3);
        System.out.println(x/=3);
        System.out.println(x%=3);
        System.out.println(x&=3);
        System.out.println(x|=3);
        System.out.println(x^=3);
        System.out.println(x>>=3);
        System.out.println(x<<=3);
    }
}
